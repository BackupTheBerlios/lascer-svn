/**
 * Enthält default-Werte für die Konfiguration der Klassen aus diesem Package.
 *
 * @author  Dietmar Lippold
 */
public class Konstanten {

    /**
     * Die Beschreibung eines unbekannten Wertes.
     */
    public static final String UNBEKANNT_BESCHREIB = "?";

    /**
     * Die Repräsentation eines unbekannten Wertes eines String-Attributs.
     */
    public static final String UNBEKANNT_STRING = "?";

    /**
     * Die Repräsentation eines unbekannten Wertes eines Nominal-Attributs.
     */
    public static final String UNBEKANNT_NOMINAL = "?";

    /**
     * Die Repräsentation eines unbekannten Wertes eines real-Attributs.
     */
    public static final float UNBEKANNT_REAL = Float.MIN_VALUE;

    /**
     * Die Repräsentation eines unbekannten Wertes eines int-Attributs.
     */
    public static final int UNBEKANNT_INT = Integer.MIN_VALUE;
}

